# Criar a classe
# chamada 'calculadora'
class Calculadora:
    
    #Criar o método
    # chamado 'soma'
    def soma(self, n1, n2):
      return n2 + n1
 
    #Criar o método
    # chamado 'subtração'
    def sub(self, n1, n2):
      return n1 - n2
     
    #Criar o método
    # chamado 'multiplicação'
    def mult(self, n1, n2):
      return n1 * n2
     
    #Criar o método
    # chamado 'divisão'
    def divi(self, n1, n2):
      return n1 / n2
  
 
 
 